a=[10,20,30,40,50,60,70,80,90]
print(a)        #[10, 20, 30, 40, 50, 60, 70, 80, 90]
print(len(a))   #9

#Indexing...
print(a[0])  #10
print(a[6])  #70
print(a[8])  #90
print(a[-1]) #90
print(a[-3]) #70

#Slicing...
print(a[:])          #[10, 20, 30, 40, 50, 60, 70, 80, 90]
print(a[4: :-1])     #[50, 40, 30, 20, 10]
print(a[1:3])        #[20, 30]
print(a[5:8])        #[60, 70, 80]
print(a[1:3])        #[20, 30]
print(a[7:])         #[80, 90]
print(a[1:3])        #[20, 30]
print(a[-7:-2])      #[30, 40, 50, 60, 70]
print(a[5:2])        #[]
print(a[:3:-1])      #[90, 80, 70, 60, 50]
print(a[6:1:-1])     #[70, 60, 50, 40, 30]
print(a[-4:8])       #[60, 70, 80]
print(a[-8::2])      #[20, 40, 60, 80]
print(a[:-5])        #[10, 20, 30, 40]
print(a[-6: : -1])   #[40, 30, 20, 10]
print(a[: : -3])     #[90, 60, 30]
print(a[-4:-9 : 1])  #[]
print(a[-4:-9 : -1]) #[60, 50, 40, 30, 20]
print(a[3:8:0])      #Error